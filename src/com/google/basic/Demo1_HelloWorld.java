// 包(其实就是文件夹，用于解决相同类名问题)
// 全部小写 一般是公司域名倒着写
// 类或者接口  如果是一个单词，首字母大写，多个单词，每个单词首字母大写 驼峰命名
// 方法和变量  如果是一个单词，全部小写，多个单词，从第二个单词首字母大写  main getName setName
// 常量  如果是一个单词，全部大写，多个单词，全部大写，用_分隔